/*
Задача 9.
Дано два списки прізвищ студентів, що відвідують гуртки з математики і історії. Підрахувати, скільки студентів з гуртка з історії також відвідують гурток з математики. Також підрахувати, скільки всього студентів відвідують хоча б один гурток.
*/

let mathClub = ["Іванов", "Шевченко", "Бульба", "Хмельницький"];
let historyClub = ["Шевченко", "Іванов", "Усик", "Українка"];

// Студенти, які відвідують обидва гуртки (перетин множин)
let mathSet = new Set(mathClub);
let historySet = new Set(historyClub);

let bothClubs = new Set(
  [...mathSet].filter((student) => historySet.has(student))
);

// Унікальні студенти з обох гуртків
let allStudents = new Set([...mathSet, ...historySet]);

console.log(
  "Кількість студентів, які відвідують обидва гуртки:",
  bothClubs.size
);
console.log(
  "Кількість унікальних студентів, які відвідують хоча б один гурток:",
  allStudents.size
);
